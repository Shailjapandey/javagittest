package StringExample;

public class CopyString {

	public static void main(String[] args) {
		
		String s1="hello";
		String s2 ="World";
		System.out.println(s1 +"  <- S1");
		System.out.println(s2 +"<- S2");
		s1="test";
		System.out.println(s1 +" A<- S1");
		System.out.println(s2 +" A<- S2");
		

	}

}
